// Function to change the language
function changeLanguage(lang) {
    console.log('changeLanguage llamada con idioma:', lang);
    
    // Si no se proporciona un idioma, alternar entre español e inglés
    if (!lang) {
        lang = window.currentLanguage === 'es' ? 'en' : 'es';
        console.log('Alternando idioma a:', lang);
    }
    
    window.currentLanguage = lang;
    localStorage.setItem('language', lang);
    localStorage.setItem('preferredLanguage', lang);
    console.log('Idioma guardado en localStorage:', localStorage.getItem('language'));
    console.log('Preferencia de idioma guardada en localStorage:', lang);
    console.log('window.currentLanguage actualizado a:', window.currentLanguage);
    
    // Asegurarse de que el DOM esté completamente cargado antes de traducir
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => {
            // Traducir la página
            console.log('Llamando a translatePage desde changeLanguage');
            translatePage();
            
            // Actualizar el botón de idioma
            console.log('Llamando a updateLanguageToggle desde changeLanguage');
            updateLanguageToggle();
        });
    } else {
        // Traducir la página
        console.log('Llamando a translatePage desde changeLanguage');
        translatePage();
        
        // Actualizar el botón de idioma
        console.log('Llamando a updateLanguageToggle desde changeLanguage');
        updateLanguageToggle();
    }
    
    console.log('changeLanguage completada');
}

// Function to translate the page
function translatePage() {
    const lang = window.currentLanguage || 'es';
    console.log('translatePage llamada con idioma:', lang);
    
    // Asegurarse de que el DOM esté completamente cargado
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => translatePageElements(lang));
    } else {
        translatePageElements(lang);
    }
}

// Función auxiliar para traducir los elementos
function translatePageElements(lang) {
    console.log('Elementos con data-translate:', document.querySelectorAll('[data-translate]').length);
    
    // Obtener todos los elementos con el atributo data-translate
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    console.log('Total de elementos a traducir:', elementsToTranslate.length);
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-translate');
        console.log('Traduciendo elemento con clave:', key, 'elemento:', element.tagName, 'id:', element.id);
        
        if (translations[lang] && translations[lang][key]) {
            // Check if it's an input placeholder
            if (element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
                console.log('Traducido placeholder:', key, 'a:', translations[lang][key]);
            } 
            // Check if it's an option value
            else if (element.tagName === 'OPTION' && element.value === '') {
                element.textContent = translations[lang][key];
                console.log('Traducido option:', key, 'a:', translations[lang][key]);
            }
            // Regular text content
            else {
                element.textContent = translations[lang][key];
                console.log('Traducido texto:', key, 'a:', translations[lang][key]);
            }
        } else {
            console.warn('No se encontró traducción para:', key, 'en idioma:', lang);
            if (!translations[lang]) {
                console.error('No existe el idioma:', lang, 'en translations');
            }
        }
    });
    console.log('Página traducida al idioma:', lang);
}

